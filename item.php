            <h2>Background</h2>
            
            <p>A bear, an eagle, badgers and buffaloes comprised the original exhibition of the Department of Living Animals on the south side of the Smithsonian Institution Building.</p>

            <p>Opened to the public in 1887, the department living exhibits gave Smithsonian taxidermists an opportunity to observe live the habits and positions of various species and to use this knowledge in mounting collections and exhibitions.</p>

            <p>Under the direction of William Hornaday, the Department of Living Animals was the forerunner of the National Zoological Park, established by an Act of Congress in 1889 for "the advancement of science, the instruction and recreation of the people."</p>
        
