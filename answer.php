        <h1>Why is there a lockkeeper's house on the Mall?</h1>
        
        <article class="answer">
        
            <p>Until 1872, a canal covered the land that is now Constitution Avenue. Between 1835 and 1855, a lockkeeper and his family lived in the small stone house at the lock where the C & O Canal Extension joined the Washington City Canal and the Chesapeake and Ohio (C & O) Canal.</p>
    
            <p>In the early nineteenth century, canals were the chief means of moving freight and bulk cargoes between ports and markets. In Washington, these waterways surrounded and criss-crossed the National Mall connecting  the Anacostia (then called the Eastern Branch) and the Potomac Rivers to each other and to a series of inland waterways.</p>
            
