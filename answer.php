                      <p>Over the decades, financial problems, conflicts between builders and contractors and between local and federal governments, floods, pestilence, right-of-way disputes, and finally, the advent of railroads, stalled canal growth and clean-up. The lockkeeper's house was abandoned in 1855.  It served for a time as a squatters' tenement and later for the park police as a holding cell for prisoners. In the 1870s, dismayed by the downtrodden condition of the nation's capital and the unsanitary conditions of the Canal, the city began to fill in the waterway, making way for today's Constitution Avenue. The Lockkeeper's House remained as a visible remembrance of the city's early commercial dreams.</p>
                      
