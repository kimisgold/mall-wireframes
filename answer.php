                <p>In 1791, decades before the lockkeeper's house was built, Pierre Charles L'Enfant first proposed constructing the Washington City Canal. L'Enfant intended the canal to supply markets built along its path, ensuring the flow of commerce in the city. Lotteries conducted to finance construction  failed to raise enough money. Finally, in 1809, Congress authorized $100,000 for the canal which formally opened in 1815, delayed by the city's destruction in the War of 1812.</p>

