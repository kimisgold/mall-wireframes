                     <h2><a href="#">Canal ("Washington Canal"), Washington, D.C. Lock details and sections</a></h2>

                      <p>By 1833, the C&O Extension connected the Washington City Canal to the Chesapeake and Ohio Canal.  In 1835, the  lockkeeper living in the newly-constructed house on the corner of present-day 17th Street and Constitution Avenues managed traffic between canals and maintained canal records. The lockkeeper raised and lowered the canal locks, likely a manually-operated sluice gate allowing him to control the flow of water for passage of boats and their cargo. Some accounts report that the lockkeeper lived in the small stone building with 13 children.</p>
                
