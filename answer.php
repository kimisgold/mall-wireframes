                      <p>Barges travelling on the Washington City Canal brought firewood and coal, marble and stone for public buildings, and food supplies, but the Canal was too shallow for large boats. Its banks frequently overflowed into surrounding streets during high tide, creating a marshy terrain on the Mall. The Canal flowed from the Anacostia River towards the Capitol, crossing the National Mall to join Tiber Creek, an inland extension of the Potomac River. It then entered the Potomac just south of the White House.</p>
                
